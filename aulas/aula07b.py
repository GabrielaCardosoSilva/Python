n1 = int(input('Digite um valor: '))
n2 = int(input('Digite outro valor: '))
print('Soma: {}'.format(n1 + n2))
print('Subtração: {}'.format(n1 - n2))
print('Produto: {}'.format(n1 * n2))
print('Divisão: {:.3}'.format(n1 / n2))  #Para exibir com até 3 casas decimais
print('Divisão inteira: {}'.format(n1 // n2))
print('Exponenciação: {}'.format(n1 ** n2))
print('Raiz: {:.3}'.format(n1 ** (1/n2)))
